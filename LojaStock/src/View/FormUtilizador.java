    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
if (txtNuit.getText().trim().equals("")) {
            lbValidacao.setVisible(true);
            lbValidacao.setText("Insira o codigo");
            txtNuit.requestFocusInWindow();
            return;
        }
        if( txtNProd.getText().trim().equals("")){
            lbValidacao.setVisible(true);
            lbValidacao.setText("Insire o nome do Vendedor");
            txtNProd.requestFocusInWindow();
            return;
         }
         if(txtuser.getText().trim().equals("")){
            lbValidacao.setVisible(true);
            lbValidacao.setText("Insira o nome de utilizador");
            txtuser.requestFocusInWindow();
            return;
          }   
          if(txtpw.getText().trim().equals("")){
             lbValidacao.setVisible(true);
            lbValidacao.setText("Insira o password");
            txtpw.requestFocusInWindow();
            return;
          }
        if(txtIdP.getText().trim().equals("")){
           VendedorJpaController cc = new VendedorJpaController(emf);
            if (cc.existeCodigo(txtNuit.getText()) != 0) {
                lbValidacao.setVisible(true);
                lbValidacao.setText("Este codigo ja existe. Introduza outro");
                txtNuit.requestFocusInWindow();
                return;
            }
           Vendedor pro = new Vendedor();
           pro.setCodigo(txtNuit.getText());
           pro.setNomecompleto(txtNProd.getText());
           pro.setEmail(txtemail.getText());
           pro.setSenha(txtpw.getText());
           pro.setTelemovel(txtcel1.getText());
           int id = cbac.getSelectedIndex();
           pro.setTipoacesso(id);
           pro.setUsername(txtuser.getText());
           new VendedorJpaController(emf).create(pro);
           limpar();
            JOptionPane.showMessageDialog(this, "Adicionado com Sucesso");
        } else {
            int respo = JOptionPane.showOptionDialog(null, "Pretende modificar os dados deste Utilizador?","ATENÇÃO!",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,   
                null,new String[]{"SIM", "NÃO"},null);
            if(respo==1||respo == -1)return;
            try {
                VendedorJpaController cc = new VendedorJpaController(emf);
                Vendedor pro = cc.findVendedor(Integer.parseInt(txtIdP.getText()));
                String c = pro.getCodigo();
                if ((!c.equals(txtNuit.getText())) && cc.existeCodigo(txtNuit.getText()) != 0) {
                    lbValidacao.setVisible(true);
                    lbValidacao.setText("Este codigo ja existe. Introduza outro");
                    txtNuit.requestFocusInWindow();
                    return;
                } 
                pro.setCodigo(txtNuit.getText());
                pro.setNomecompleto(txtNProd.getText());
           pro.setEmail(txtemail.getText());
           pro.setSenha(txtpw.getText());
           pro.setTelemovel(txtcel1.getText());
           int id = cbac.getSelectedIndex();
           pro.setTipoacesso(id);
           pro.setUsername(txtuser.getText());
                new VendedorJpaController(emf).edit(pro);
                JOptionPane.showMessageDialog(this, "Editado com Sucesso");
                this.setVisible(false);
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(FormProduto.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(FormProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
